<template>
  <b-card :header="`${userTagLabel} Metrics (Top 20)`" body-class="p-0" data-cy="quizUserTagsChart">
    <div style="max-height: 400px; overflow-y: auto; overflow-x: clip;" class="pt-2 pr-2">
    <metrics-overlay :loading="loading" :has-data="hasData" no-data-msg="No data yet...">
        <apexchart v-if="!loading" type="bar" :height="chartOptions.chart.height" :options="chartOptions" :series="series"></apexchart>
    </metrics-overlay>
    </div>
  </b-card>
</template>

<script>
  import numberFormatter from '@/filters/NumberFilter';
  import MetricsOverlay from '@/components/metrics/utils/MetricsOverlay';
  import QuizService from '@/components/quiz/QuizService';
  import UserTagsConfigMixin from '@/components/users/UserTagsConfigMixin';

  export default {
    name: 'QuizUserTagsChart',
    mixins: [UserTagsConfigMixin],
    components: { MetricsOverlay },
    data() {
      return {
        loading: true,
        hasData: false,
        quizId: this.$route.params.quizId,
        series: [],
        chartOptions: {
          chart: {
            height: 350,
            width: 250,
            type: 'bar',
            toolbar: {
              show: true,
              offsetX: -13,
              offsetY: 0,
            },
          },
          tooltip: {
            y: {
              formatter(val) {
                return numberFormatter(val);
              },
            },
          },
          plotOptions: {
            bar: {
              horizontal: true,
              barHeight: '40%',
              dataLabels: {
                position: 'bottom',
              },
              distributed: true,
            },
          },
          stroke: {
            show: true,
            width: 2,
            colors: ['transparent'],
          },
          xaxis: {
            categories: [],
            title: {
              text: '# of runs',
            },
            labels: {
              formatter(val) {
                return Math.trunc(val);
              },
            },
          },
          yaxis: {
            title: {
              text: '',
            },
            labels: {
              show: false,
            },
          },
          dataLabels: {
            enabled: true,
            textAnchor: 'start',
            offsetX: 0,
            formatter(val, { seriesIndex, dataPointIndex, w }) {
              return `${w.globals.seriesX[seriesIndex][dataPointIndex]}: ${numberFormatter(val)}`;
            },
            style: {
              colors: ['#17a2b8'],
              fontSize: '14px',
              fontFamily: 'Helvetica, Arial, sans-serif',
              fontWeight: 'bold',
            },
            dropShadow: {
              enabled: true,
            },
            background: {
              enabled: true,
              foreColor: '#ffffff',
              padding: 10,
              borderRadius: 2,
              borderWidth: 1,
              borderColor: '#686565',
              opacity: 1,
              dropShadow: {
                enabled: false,
              },
            },
          },
          legend: {
            show: false,
          },
        },
      };
    },
    mounted() {
      this.loadData();
    },
    methods: {
      loadData() {
        this.loading = true;
        QuizService.getUserTagCounts(this.quizId, 'dutyOrganization')
          .then((res) => {
            const seriesData = res.map((item) => ({ x: item.value, y: item.count }));
            this.series = [{ data: seriesData, name: '# of Runs' }];
            this.hasData = seriesData.length > 0;
            this.chartOptions.chart.height = this.hasData ? 150 + (seriesData.length * 50) : 350;
          }).finally(() => {
            this.loading = false;
          });
      },
    },
  };
</script>

